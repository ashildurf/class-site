var $frames = $('.title');

// switch active frames
$frames.mousedown(function() {
  var $this = $(this);
  if ($this.hasClass('inactive')) {
    var $active = $('.title.active');
    var scrollTop = $(document).scrollTop();
    $active
      .attr('data-scroll', scrollTop)
      .removeClass('active')
      .addClass('inactive')
      .css('top', -scrollTop+'px');
    
    $this
      .removeClass('inactive')
      .addClass('active')
      .css('top', '');
    $(document).scrollTop($this.attr('data-scroll') || 0);
  }
});



